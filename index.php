<h2 class="mtl">Les Règles de la compétition</h2>
<p><strong>Pour la première fois en championnat d'Europe, il y aura 24 équipes au lieu de 16.</strong> Elles sont réparties en 6 groupes par tirage au sort.</p>
<p>Pour la première fois aussi, il y aura des huitièmes de finale.</p>
<ul id="rules">
<li><strong>Phase de poule</strong>&nbsp;: dans chaque groupe, chaque équipe rencontre les 3 autres équipes de son groupe.<br>victoire = 3 points<br>match nul = 1 point<br>défaite = 0 point<br>Les 2 équipes qui ont le plus de points accèdent aux 8èmes de finale, ainsi que les 4 équipes meilleures troisièmes.</li>
<li><strong>Huitièmes de finale</strong>&nbsp;: chaque Premier d'un groupe rencontre le Second d'un autre groupe. Les vainqueurs accèdent aux demi-finales.</li>
<li><strong>Quarts de finale</strong>&nbsp;: les vainqueurs accèdent aux demi-finales.</li>
<li><strong>Demi-finales</strong>&nbsp;: les vainqueurs accèdent à la finale. Il n'y a pas de match pour départager le 3ème et le 4ème.</li>
<li><strong>Finale</strong>&nbsp;: le gagnant sera le champion d'Europe 2016.</li>
</ul>

